package college;

public class Address {
private String Hnumber;
private String Ps;
private int pin;
public String getHnumber() {
	return Hnumber;
}
public String getPs() {
	return Ps;
}
public int getPin() {
	return pin;
}

public void setHnumber(String hnumber)
{
	this.Hnumber=hnumber;
}
public void setPs(String ps)
{
	this.Ps=ps;
}
public void setPin(int pin)
{
	this.pin=pin;
}

}
